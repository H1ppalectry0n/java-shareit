DROP TABLE IF EXISTS comments CASCADE;
DROP TABLE IF EXISTS bookings CASCADE;
DROP TABLE IF EXISTS items CASCADE;
DROP TABLE IF EXISTS requests CASCADE;
DROP TABLE IF EXISTS users CASCADE;

CREATE TABLE IF NOT EXISTS users (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
name VARCHAR(255) NOT NULL,
email VARCHAR(512) NOT NULL,

CONSTRAINT pk_user PRIMARY KEY (id),
CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS requests (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
description TEXT NOT NULL,
requestor_id BIGINT NOT NULL,

CONSTRAINT pk_request PRIMARY KEY (id),
FOREIGN KEY (requestor_id) REFERENCES users(id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS items (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
name VARCHAR(255) NOT NULL,
description TEXT NOT NULL,
is_available BOOLEAN NOT NULL DEFAULT FALSE,
owner_id BIGINT NOT NULL,
request_id BIGINT,

CONSTRAINT pk_item PRIMARY KEY (id),
FOREIGN KEY (owner_id) REFERENCES users(id) ON DELETE RESTRICT,
FOREIGN KEY (request_id) REFERENCES requests(id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS bookings (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
start_date TIMESTAMP WITHOUT TIME ZONE NOT NULL CHECK (start_date >= CURRENT_TIMESTAMP),
end_date TIMESTAMP WITHOUT TIME ZONE NOT NULL CHECK (end_date > start_date),
item_id BIGINT NOT NULL,
booker_id BIGINT NOT NULL,
status VARCHAR(50) NOT NULL,

CONSTRAINT pk_booking PRIMARY KEY (id),
FOREIGN KEY (item_id) REFERENCES items(id) ON DELETE RESTRICT,
FOREIGN KEY (booker_id) REFERENCES users(id) ON DELETE RESTRICT
);

CREATE TABLE IF NOT EXISTS comments (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
text TEXT NOT NULL,
item_id BIGINT NOT NULL,
author_id BIGINT NOT NULL,
created TIMESTAMP WITHOUT TIME ZONE NOT NULL,

CONSTRAINT pk_comment PRIMARY KEY (id),
FOREIGN KEY (item_id) REFERENCES items(id) ON DELETE RESTRICT,
FOREIGN KEY (author_id) REFERENCES users(id) ON DELETE RESTRICT
);
